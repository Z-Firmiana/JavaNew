public class ValueDemo4{
	public static void main(String[] args){
		//byte
		byte a = 10;
		//short
		short b = 20;
		//int
		int c = 30;
		//long
		long d = 9999999999L;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		
		//float
		float e = 10.2F;
		//double
		double f = 10.9;
		System.out.println(e);
		System.out.println(f);

	}
}
